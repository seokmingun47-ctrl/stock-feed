// 태그 정리: 앞 # 제거, 공백 정리, 20자 제한
export function normalizeTag(raw: string): string {
  return String(raw ?? "")
    .trim()
    .replace(/^#+/, "")
    .replace(/\s+/g, " ")
    .slice(0, 20)
    .trim();
}

