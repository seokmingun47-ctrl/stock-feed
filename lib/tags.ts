// 로컬 빠른 판별: 용어집 · 티커패턴 · 금융성 접미어
export function localFinanceMatch(tag: string): boolean {
  const t = tag.toLowerCase().replace(/\s+/g, "");
  if (!t) return false;
  if (FINANCE_TERMS.has(t)) return true;
  // 국내 종목코드 6자리 / 미국 티커 1~5자 대문자
  if (/^\d{6}$/.test(tag)) return true;
  if (/^[A-Za-z]{1,5}$/.test(tag) && tag === tag.toUpperCase()) return true;
  // 금융성 어미가 붙은 합성어 (예: 반도체주, 방산테마, 배당ETF)
  if (/(주|증시|투자|테마|섹터|지수|etf|코인|반도체|배터리|바이오)$/i.test(t)) return true;
  return false;
}
